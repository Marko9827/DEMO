$(document).ready(function () {

    //Dropdown
    $(".dropdown").hover(
        function () {
            $(this).addClass("open");
            $(".dropdown-toggle").attr("aria-expanded", "true");
        },
        function () {
            $(this).removeClass("open");
            $(".dropdown-toggle").attr("aria-expanded", "false");
        }
    );
    // End of Dropdown

    //Search Event
    $( ".headerSearchToggle" ).click(function() {
        $(".headerSearch").toggleClass("headerSearchShow");
    });

    // End of Search Event

    // Carousel
    $('#carousel-index').carousel({
        interval: 3000,
        wrap: false
    })
    // End of Carousel

});